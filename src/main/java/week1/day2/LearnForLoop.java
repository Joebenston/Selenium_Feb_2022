package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
	
		
		//Want to print 1 to 10 ; 1,2,3,4,5
		//initialize ; condition ; increment / decrement
		//i++ -> i = i+1;
		
		//Want to print 5, 4, 3 , 2, 1
		
		for (int i = 5; i > 0 ; i--) {
			System.out.println(i);
		}
		

	}

}
