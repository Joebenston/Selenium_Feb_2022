package week1.day2;

public class IdentifyOddOrEven {

	public static void main(String[] args) {
	int input = 10; // if divisible by 2 - even else odd number
	
	if(input % 2 == 0)
		System.out.println("Given number is even");
	else
		System.out.println("Given number is odd");
		

	}

}
